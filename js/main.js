var q4 = {
    _init: function() {
        var inst = this;

        inst._onMenuClick();
        inst._onMenuClose();
        inst._onHeroScroll();
        inst._onLogoClick();
        inst._onMenuItemClick($('html, body'));
    },
    _onMenuClick: function() {
        $('.nav-toggle').on('click', function(){
            $('body').toggleClass('toggle-nav-open');
        });
    },
    _onMenuClose: function() {
        $('.nav-container').on('click', '.close', function(){
            $('body').toggleClass('toggle-nav-open');
        });
    },
    _onMenuItemClick: function(container) {
        $('.toggle-nav').on('click', 'a', function(){

            container.animate({
                scrollTop: $( $.attr(this, 'href') ).offset().top
            }, 500, 'linear', function(){
                $('body').removeClass('toggle-nav-open');
            });

            location.hash = $.attr(this, 'href');
            return false;
        });
    },
    _onHeroScroll: function() {
        var inst = this;
        $('.scroll-button').on('click', function(){
            inst._letScroll( $('.content-continer').offset().top );
        });
    },
    _onLogoClick: function() {
        var inst = this;
        $('.nav-logo').on('click', function(){
            inst._letScroll( 0 );
            $(window).resize();
        });
    },
    _letScroll: function(position){
        $('html, body').animate({
            scrollTop: position
        }, 1000);
    },
    _isElementInViewport: function(el) {
        el = el[0];
        if (el === undefined) return false
        var rect = el.getBoundingClientRect();

        return ( 
            rect.top >= 0 && 
            rect.left >= 0 && 
            rect.bottom + 250 <= $(window).height() && 
            rect.right <= $(window).width()
        );
    },
    _onVisibilityChange:function (el) {
        var inst = this;

        return function () {
            el.each(function(i){
                if ( inst._isElementInViewport( $(this) ) ) {
                    if (!$(this).parent().hasClass('animate')){
                        $(this).parent().addClass('animate');

                        if ($(this).parent().find('.count-to').length && navigator.appVersion.indexOf("MSIE 8.") == -1){
                            $(this).parent().find('.timer').countTo({
                                formatter: function (value, options) {
                                    return value.toFixed($(this).data('decimal') !== undefined ? $(this).data('decimal') : 0);
                                }
                            });
                        }
                    }
                }
            });
        }
    },
    _setHeight: function($el) {
        var height = $(window).height();

        $el.css({
            height: height + 5
        });

        if ($(window).width() > height*2) {
            $el.find('img').css({
                height: height,
                width: 'auto'
            });
        } else {
            $el.find('img').css({
                height: 'auto',
                width: '100%'
            });
        }
    }
}

